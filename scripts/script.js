// Handle position & education input
function positionInputShow() {
    let input = document.getElementsByClassName("position-input")[0];
    let link = document.getElementsByClassName("position-input-link")[0];
    let del = document.getElementsByClassName("position-delete-link")[0];
    // console.log(input);
    input.style.display = "block";
    link.style.display = "none";
    del.style.display = "block";
}
function positionInputHide() {
    let input = document.getElementsByClassName("position-input")[0];
    let link = document.getElementsByClassName("position-input-link")[0];
    let del = document.getElementsByClassName("position-delete-link")[0];
    // console.log(input);
    input.style.display = "none";
    link.style.display = "block";
    del.style.display = "none";
}
